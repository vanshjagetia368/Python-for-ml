#Starting with variable and data types in python
#Variable is a container for storing data values. It is a way to label and store data in memory. In Python, you can create a variable by assigning a value to it using the equals sign (=).
#For example:
x = 5
y = "Hello, World!"
#In this example, we have created two variables: x and y. The variable x is assigned the value 5, which is an integer, and the variable y is assigned the value "Hello, World!", which is a string.
#Python has several built-in data types, including:
#1. Numeric types: int, float, complex
#2. Sequence types: list, tuple, range
#3. Text type: str
#4. Mapping type: dict
#5. Set types: set, frozenset
#6. Boolean type: bool
#7. Binary types: bytes, bytearray, memoryview
#You can use the type() function to check the data type of a variable. For example
print(type(x))  # Output: <class 'int'>
print(type(y))  # Output: <class 'str'>
#You can also change the value of a variable by reassigning it. For example:
x = 10
print(x)  # Output: 10
#In this example, we have reassigned the value of x to 10. Now,x holds the value 10 instead of 5.
#Python is a dynamically typed language, which means that you don't need to declare the data type of a variable when you create it. The data type is inferred based on the value assigned to the variable. This allows for flexibility in programming, but it also means that you need to be careful when working with variables to avoid type-related errors.
#In summary, variables are used to store data values in Python, and there are various data types available to represent different kinds of data. Understanding variables and data types is fundamental to programming in Python.
